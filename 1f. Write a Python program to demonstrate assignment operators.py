# Program to demonstrate assignment operators

a = 10
print("Initial value of a:", a)

# Assignment operators
a += 5   # Add and assign
print("After a += 5 :", a)

a -= 3   # Subtract and assign
print("After a -= 3 :", a)

a *= 2   # Multiply and assign
print("After a *= 2 :", a)

a /= 4   # Divide and assign
print("After a /= 4 :", a)

a %= 3   # Modulus and assign
print("After a %= 3 :", a)

a **= 2  # Exponent and assign
print("After a **= 2 :", a)

a //= 2  # Floor divide and assign
print("After a //= 2 :", a)